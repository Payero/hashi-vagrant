#!/bin/bash

NIC="eth1"
IP=`ifconfig ${NIC} | grep -Eo 'inet (addr:)?([0-9]*\.){3}[0-9]*' | grep -Eo '([0-9]*\.){3}[0-9]*' | grep -v '127.0.0.1'`
NAME=`hostname`

echo "*******************************************************"
echo "        ${NAME}:  ${IP}"
echo "*******************************************************"

